def slug_part(text: str) -> str:
    code = DOMAIN_CODES.get(text)
    if code:
        return code
    s = re.sub(r"[^a-z0-9]+", "-", text.lower()).strip("-")
    return s or "topic"


def bloom_for(subject: str, grade: str, domain: str) -> str:
    n = int(grade[1:])
    if subject == "zh" and domain in {"表达与交流", "整本书阅读"}:
        return "create" if n >= 7 else "apply"
    if n <= 2:
        return "remember"
    if n <= 4:
        return "understand"
    if n <= 6:
        return "apply"
    return "analyze"


def make_description(subject_name: str, grade: str, domain: str, unit: str, title: str) -> str:
    return f"{subject_name}{grade.upper()}「{unit}」中的核心知识点：{title}，用于支撑{domain}领域的学习、练习与诊断。"


def make_objectives(subject: str, title: str, domain: str) -> list[str]:
    if subject == "math":
        return [f"理解{title}的概念或规则", f"能运用{title}解决基础问题", "能说明关键步骤和易错点"]
    if subject == "zh":
        return [f"能识别并理解{title}", f"能在阅读或表达中运用{title}", "能结合语境说明表达效果"]
    if subject == "en":
        return [f"能听懂或读懂{title}相关语言材料", f"能在真实语境中使用{title}", "能完成相应的口头或书面表达任务"]
    return [f"能描述{title}相关现象", f"能基于证据解释{title}", "能在探究活动中记录、比较并交流结论"]


def make_exam_points(subject: str, title: str) -> list[str]:
    if subject == "math":
        return [title, "概念辨析", "步骤规范", "实际情境应用"]
    if subject == "zh":
        return [title, "信息提取", "语境理解", "表达运用"]
    if subject == "en":
        return [title, "词句理解", "情境交际", "阅读或写作迁移"]
    return [title, "现象解释", "实验探究", "证据推理"]


def build_subject(subject: str) -> dict:
    meta = SUBJECT_META[subject]
    grades = {}
    all_kps = []
    previous_by_domain: dict[str, str] = {}
    for grade, units in CURRICULUM[subject].items():
        grade_kps = []
        domain_counts = defaultdict(int)
        order = 1
        for unit_idx, (domain, unit, topics) in enumerate(units, start=1):
            unit_slug = f"{slug_part(domain)}-{unit_idx:02d}"
            for topic_idx, title in enumerate(topics, start=1):
                kp_id = f"kp-{subject}-{grade}-{unit_slug}-{topic_idx:02d}"
                prerequisites = []
                if domain in previous_by_domain:
                    prerequisites.append(previous_by_domain[domain])
                kp = {
                    "id": kp_id,
                    "subject": subject,
                    "grade": grade,
                    "stage": "义务教育",
                    "domain": domain,
                    "unit": unit,
                    "title": title,
                    "description": make_description(meta["name"], grade, domain, unit, title),
                    "objectives": make_objectives(subject, title, domain),
                    "key_exam_points": make_exam_points(subject, title),
                    "prerequisites": prerequisites,
                    "difficulty_coordinate": {"grade": grade, "bloom": bloom_for(subject, grade, domain)},
                    "source": {
                        "type": "official_standard",
                        "name": meta["standard"],
                        "url": OFFICIAL_STANDARD_URL,
                        "note": "依据课程标准领域/学段要求整理，并结合常见教材单元顺序细化为内置知识点索引。",
                    },
                    "order": order,
                }
                grade_kps.append(kp)
                all_kps.append(kp)
                previous_by_domain[domain] = kp_id
                domain_counts[domain] += 1
                order += 1
        grades[grade] = {
            "summary": {
                "kp_count": len(grade_kps),
                "domains": dict(sorted(domain_counts.items())),
            },
            "knowledge_points": grade_kps,
        }
    return {
        "schema_version": "curriculum-full/v1",
        "subject": subject,
        "subject_name": meta["name"],
        "stage": "义务教育",
        "standard": {
            "name": meta["standard"],
            "url": OFFICIAL_STANDARD_URL,
        },
        "note": meta["note"],
        "coverage": {
            "grades": list(CURRICULUM[subject].keys()),
            "kp_count": len(all_kps),
            "domains": sorted({kp["domain"] for kp in all_kps}),
        },
        "grades": grades,
    }


def write_subject_json(subject: str, data: dict) -> None:
    FULL_DIR.mkdir(parents=True, exist_ok=True)
    path = FULL_DIR / f"{subject}.g1-g9.2022.json"
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def write_subject_md(subject: str, data: dict) -> None:
    MD_DIR.mkdir(parents=True, exist_ok=True)
    meta = SUBJECT_META[subject]
    lines = [
        f"# {meta['name']} 课纲与知识点（内置完整库索引）",
        "",
        f"> subject code: `{subject}`。本文件由 `scripts/build_curriculum_full.py` 从结构化库生成。",
        f"> 结构化源：`references/curriculum_full/{subject}.g1-g9.2022.json`。",
        f"> 依据：{meta['standard']}；用于 G9 兼容解析和人工快速浏览。",
        "",
    ]
    for grade, grade_data in data["grades"].items():
        lines.append(f"## {grade}")
        current_domain = None
        for kp in grade_data["knowledge_points"]:
            if kp["domain"] != current_domain:
                current_domain = kp["domain"]
                lines.append(f"\n### {current_domain}")
            lines.append(f"- `{kp['id']}` {kp['title']} — {kp['unit']}")
            if kp["prerequisites"]:
                refs = "、".join(f"`{p}`" for p in kp["prerequisites"])
                lines.append(f"  - 前置：{refs}")
            lines.append(f"  - 考点：{'、'.join(kp['key_exam_points'])}")
        lines.append("")
    (MD_DIR / f"{'chinese' if subject == 'zh' else 'english' if subject == 'en' else subject}.md").write_text(
        "\n".join(lines).rstrip() + "\n",
        encoding="utf-8",
    )


def main() -> int:
    for subject in ("math", "zh", "en", "science"):
        data = build_subject(subject)
        write_subject_json(subject, data)
        write_subject_md(subject, data)
        print(f"{subject}: {data['coverage']['kp_count']} knowledge points")
    return 0


